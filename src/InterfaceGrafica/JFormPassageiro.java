    private void btn_reservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_reservaActionPerformed
        try {
            //Fazer a reserva:
            
            ConsumindoServicos http = new ConsumindoServicos();
            String motorista_id = txt_idmotorista.getText();
            
            //Buscando o motorista por meio di ID
            String chamadaWS_motorista = "http://127.0.0.1:5000/motoristas/"+motorista_id;
            String json_motorista = http.envioGet(chamadaWS_motorista);
            Motorista m = new Motorista();
            Gson gm = new Gson();
            java.lang.reflect.Type motoristaType = new TypeToken<Motorista>() {}.getType();
            m = gm.fromJson(json_motorista, motoristaType);
            //Verifica a disponibilidade do motorista:
            if(m.getDisponibilidade().equals("Sim")){
                //Altera a disponibilidade do motorista
                m.setDisponibilidade("Nao");
                java.lang.reflect.Type motorista_novo_Type = new TypeToken<Motorista>() {}.getType();
                String novo_json = gm.toJson(m, motorista_novo_Type);
                String url = "http://127.0.0.1:5000/motoristas/"+motorista_id;
                //Atualiza os dados do motorista por meio do m√©todo PUT
                http.envioPost(url, novo_json, "PUT");
                String id = txt_id.getText();
                String chamadaWS = "http://127.0.0.1:5000/passageiros/"+id;
                String json = http.envioGet(chamadaWS);
                System.out.println(json);
                Passageiro p = new Passageiro();
                Gson g = new Gson();
                java.lang.reflect.Type passageiroType = new TypeToken<Passageiro>() {}.getType();
                p = g.fromJson(json, passageiroType);

                //Fazendo a reserva utilizando os objetos de passageiro e motorista obtidos acima. 
                Reserva nova_reserva = new Reserva();
                nova_reserva.setReserva_id(txt_reserva.getText());

                nova_reserva.setPassageiro_id(txt_id.getText());
                nova_reserva.setMotorista_id(txt_idmotorista.getText());
                nova_reserva.setPassageiro(p.getNome());
                nova_reserva.setMotorista(m.getNome());
                nova_reserva.setOrigem(p.getOrigem());
                nova_reserva.setDestino(p.getDestino());
                nova_reserva.setTipo_carro(m.getTipo_carro());
                nova_reserva.setCapacidade(m.getCapacidade());
                nova_reserva.setPreco(m.getPreco());
                java.lang.reflect.Type reserva_novo_Type = new TypeToken<Reserva>() {}.getType();
                String novo_json_reserva = g.toJson(nova_reserva, reserva_novo_Type);
                String url_reserva = "http://127.0.0.1:5000/reservas/"+nova_reserva.getReserva_id();
                //Enviando a reserva para ser processada. 
                http.envioPost(url_reserva, novo_json_reserva, "POST");
                JOptionPane.showMessageDialog(null, "RESERVA REALIZADA!!: ");
            }else{
                JOptionPane.showMessageDialog(null, "MOTORISTA RESERVADO!: ");
            }
                

            } catch (Exception ex) {
                Logger.getLogger(JFormPassageiro.class.getName()).log(Level.SEVERE, null, ex);
            }

    }//GEN-LAST:event_btn_reservaActionPerformed

