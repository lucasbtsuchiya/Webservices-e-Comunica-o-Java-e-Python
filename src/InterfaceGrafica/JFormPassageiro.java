    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        try {
            // Cadastrando um passageiro 
            //Instanciando o objeto do tipo Passageiro
            Passageiro novo_passageiro = new Passageiro();
            
            //Pegado os dados do formul√°rio 
            novo_passageiro.setPassageiro_id(txt_id.getText());
            novo_passageiro.setNome(txt_nome.getText());
            novo_passageiro.setOrigem(txt_origem.getText());
            novo_passageiro.setDestino(txt_destino.getText());
            novo_passageiro.setTipo_carro(txt_carro.getText());
            novo_passageiro.setCapacidade(txt_capacidade.getText());
            novo_passageiro.setPreco(txt_preco.getText());
            
            //Convertendo o objeto para um formato json
            Gson g = new Gson();
            java.lang.reflect.Type passageiro_novo_Type = new TypeToken<Passageiro>() {}.getType();
            String novo_json = g.toJson(novo_passageiro, passageiro_novo_Type);
            String url = "http://127.0.0.1:5000/passageiros/"+novo_passageiro.getPassageiro_id();
            ConsumindoServicos http = new ConsumindoServicos();
            //Chamando o m√©todo para enviar os dados para o servidor
            http.envioPost(url, novo_json, "POST");
        } catch (Exception ex) {
            Logger.getLogger(JFormPassageiro.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_cadastrarActionPerformed

